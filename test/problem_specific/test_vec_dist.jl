function distance_algo(x, y)
    # v-v
    v = subtract_vector(x[1], x[2])
    # square element wise 
    v = mult_vector(v, v)
    # sum
    s = reduce_sum(v)
    s = power_of(s, 0.5)
    return isapprox(s, y[1], atol = 1e-4)
end

